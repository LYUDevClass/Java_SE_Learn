package basic.leasson3;

public class SwichDemo {

    public static void main(String[] args){

        String str1 = "D";

        switch (str1)
        {
            case "A": System.out.println("我是A");break;
            case "B": System.out.println("我是B");
            case "C": System.out.println("我是C");
            default:System.out.println("我是default");
        }

    }
}
